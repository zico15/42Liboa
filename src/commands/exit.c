#include <ft_pipex.h>

static int	*ft_input(t_command *previou, t_command *this)
{
	terminal()->destroy("exit");
	close(this->fd[1]);
	next_command(previou, this);
	return (this->fd);
}

t_command	*new_exit(void)
{	
	t_command	*c;

	c = new_command();
	if (!c)
		return (0);
	c->input = ft_input;
	return (c);
}
