#include <ft_pipex.h>

static int	*ft_input(t_command *previou, t_command *this)
{
	char	**str;

	if (string().size_list(this->commands) > 1)
	{
		str = string().split(this->commands[1], "=");
		if (string().size_list(str) > 1)
			(hashmap(terminal()->envp))->put(str[0], str[1]);
		if (str)
			printf("export: %s\n", *str);
	}
	close(this->fd[1]);
	data()->envp = hashmap(terminal()->envp)->to_str();
	next_command(previou, this);
	close(previou->fd[0]);
	close(previou->fd[1]);
	return (this->fd);
}

t_command	*new_export(char *arg)
{	
	t_command	*c;

	c = new_command(arg);
	if (!c)
		return (0);
	c->input = ft_input;
	return (c);
}
